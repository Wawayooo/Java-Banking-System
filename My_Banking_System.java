    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model =  (javax.swing.table.DefaultTableModel) My_Table.getModel();
     
        int count = 1;
        model.setRowCount(0); 
         if (BankTrans_SearchField.getText().equals("")){
             JOptionPane.showMessageDialog(null, "PLEASE FILL OFF THE ENTRY SEARCH !!");
         }else{
            try{
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/bank_system", "root", "");
                Statement query = conn.createStatement();
                String q = "select * from transaction where bank_account = "+BankTrans_SearchField.getText();
                ResultSet result = query.executeQuery(q);
                while(result.next()){
                    Object[] data = {count, result.getInt("bank_account"),result.getString("customer_name").toString(),result.getInt("desposit"), result.getInt("widthdraw"), result.getInt("current_balance"),result.getString("date").toString() };
                    model.addRow(data);
                    count++;
                }
       
            }catch(Exception e){
                JOptionPane.showMessageDialog(rootPane, "ERROR ON UPDATE TABLE: "+e);
            }
         }
    }//GEN-LAST:event_jButton12ActionPerformed

