    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(OpenBank_SearchField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "PLEASE FILL OFF THE TEXTFIELD");
            return;
        }
        try{
          Integer.parseInt(OpenBank_SearchField.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Enter a number");
            return;
        }
        try{
             Connection conn = DriverManager.getConnection(connString, user, pass);
             Statement query = conn.createStatement();
             ResultSet res = query.executeQuery("SELECT * from accounts where account_number = "+OpenBank_SearchField.getText());
             while(res.next()){
             if(res.getString("customer_name").toString().length() > 0){
                JOptionPane.showMessageDialog(rootPane, "SEARCH SUCCESS !!, RESULT FOUND !");
                id = res.getInt("id");
                OpenBank_CustomerName.setText(res.getString("customer_name").toString());
                OpenBank_AccountNo.setText(String.valueOf(res.getInt("account_number")));
                if(res.getString("sex").toString().equals("Male")){
                    OpenBank_SpecifySex.setSelectedIndex(0);
                }
                if(res.getString("sex").toString().equals("Female")){
                    OpenBank_SpecifySex.setSelectedIndex(1);
                }
                if (res.getString("sex").toString().equals("E-male")){
                    OpenBank_SpecifySex.setSelectedIndex(2);
                }
                if (res.getString("sex").toString().equals("G-male")){
                    OpenBank_SpecifySex.setSelectedIndex(3);
                }
                if (res.getString("sex").toString().equals("I'm a Mechanic")){
                    OpenBank_SpecifySex.setSelectedIndex(4);
                }
                 if(res.getString("branch").toString().equals("BRANCH NI BRIOL")){
                    OpenBank_SpecifyBranch.setSelectedIndex(0);
                }
                  if(res.getString("branch").toString().equals("BRANCH KO")){
                    OpenBank_SpecifyBranch.setSelectedIndex(1);
                }
                  if(res.getString("branch").toString().equals("BRANCH WWW")){
                    OpenBank_SpecifyBranch.setSelectedIndex(2);
                }  
                  OpenBank_BalanceField.setText(String.valueOf(res.getInt("balance")));
                return;
             }
             }
              JOptionPane.showMessageDialog(rootPane, "ACCOUNT NUMBER DOES NOT EXIST");
             
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "ERROR ON SEARCH ACCOUNT: "+e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

